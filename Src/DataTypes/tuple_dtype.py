# in tuple there are only two methods that are count and index only
# we cant modify the tuple after assigning the values in tuple data

# syntax: tuple.count(element)
# params: element
# return the count of the element that how many times it repeats in the data
tup_data = ('a','b','c')
print(tup_data.count('a'))


# syntax: tuple.index(element)
# params: element
# returns the index by the element
print(tup_data.index('c'))
